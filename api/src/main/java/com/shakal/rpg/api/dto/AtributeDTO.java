package com.shakal.rpg.api.dto;

public class AtributeDTO {

	private String name;
	private int value;
	private int modfier;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}
	public int getModfier() {
		return modfier;
	}
	public void setModfier(int modfier) {
		this.modfier = modfier;
	}
	
	
}
